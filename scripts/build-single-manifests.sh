#!/bin/bash

set -o errexit
set -o nounset
set -o pipefail

REPO_ROOT=$(dirname ${BASH_SOURCE})/..

cd "${REPO_ROOT}"

# kustomize expests parameters:
# Params:
#   - $1 - Path to the configuration file
#   - $2 - Path to resulting file
kustomize(){
    echo -e '# Generated by build-single-manifest.sh. DO NOT EDIT.\n' > "${2}"
    "${REPO_ROOT}/bin/kustomize" build "${1}" >> "${2}"
}


kustomize 'config/base' 'deploy/single/all-in-one-dbless-legacy.yaml'
kustomize 'config/variants/postgres' 'deploy/single/all-in-one-postgres.yaml'
kustomize 'config/variants/enterprise' 'deploy/single/all-in-one-dbless-k4k8s-enterprise.yaml'
kustomize 'config/variants/enterprise-postgres' 'deploy/single/all-in-one-postgres-enterprise.yaml'
kustomize 'config/variants/multi-gw/oss' 'deploy/single/all-in-one-dbless.yaml'
kustomize 'config/variants/multi-gw/enterprise' 'deploy/single/all-in-one-dbless-enterprise.yaml'
kustomize 'config/variants/konnect/oss' 'deploy/single/all-in-one-dbless-konnect.yaml'
kustomize 'config/variants/konnect/enterprise' 'deploy/single/all-in-one-dbless-konnect-enterprise.yaml'
